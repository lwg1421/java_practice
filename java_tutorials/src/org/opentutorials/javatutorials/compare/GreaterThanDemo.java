package org.opentutorials.javatutorials.compare;

public class GreaterThanDemo {

	public static void main(String[] args) {
		// > 대소를 물어보는 연산자
		// True or False를 뱉음
		System.out.println(10>20);
		System.out.println(10>2);
		System.out.println(10>10);
		
		// >= 대소를 물어보는 연산자
		// True or False를 뱉음
		System.out.println(10>=20);
		System.out.println(10>=2);
		System.out.println(10>=10);

	}

}

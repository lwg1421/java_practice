package org.opentutorials.javatutorials.condition;

public class elseifPractice {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		if (a>b) {
			System.out.println("a가 b 보다 큼");
		} else if(a==b) {
			System.out.println("a와 b는 같음");
		} else {
			System.out.println("a가 b보다 작음");
		}

	}

}

package org.opentutorials.javatutorials.operator;

public class DivisionDemo {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		float c = 10.0F;
		float d = (float)3.0;
		
		// 정수 연산 결과
		System.out.println(a/b);
		
		// 실수와 실수 연산 결과
		System.out.println(c/d);
		
		//정수와 실수 연산 결과
		System.out.println(c/b);
	}

}
